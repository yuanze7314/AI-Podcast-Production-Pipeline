/**
 * ============================================================================
 * AI Agent System Prompts 模板文件
 * ============================================================================
 *
 * 功能概述：
 * 本模块定义了AI播客生成系统中使用的所有System Prompt模板。
 * 这些Prompt用于指导不同角色的AI Agent完成各自的任务。
 *
 * ============================================================================
 * 核心概念说明：
 * ============================================================================
 *
 * System Prompt（系统提示）：
 *   是发送给大语言模型（LLM）的系统级指令，
 *   用于设定AI的角色、行为规范和输出格式。
 *
 * 在本项目中的使用方式：
 *   1. toneAnalystPrompt - 评论分析师：分析读者评论，确定播客基调
 *   2. chapterMinerPrompt - 内容矿工：从章节中提取逻辑原子和金句
 *   3. screenwriterPrompt - 编剧：将知识转化为对话脚本
 *   4. directorPrompt - 导演：润色对话，增加口语化表达
 *   5. censorPrompt - 审核员：内容安全和质量检查
 *
 * ============================================================================
 * Prompt设计原则：
 * ============================================================================
 *
 * 1. 角色清晰 - 明确AI扮演的角色和职责
 * 2. 任务具体 - 清晰描述输入、输出和处理规则
 * 3. 格式规范 - 使用JSON格式确保输出结构化
 * 4. 约束明确 - 设定边界条件和禁止事项
 * 5. 示例引导 - 通过示例说明期望的输出风格
 *
 * @module system-prompts
 * @author AI Podcast Generator
 * @version 1.0.0
 */

